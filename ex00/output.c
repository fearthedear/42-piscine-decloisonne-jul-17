#include <unistd.h>

void	printoutput(int array[][9])
{
	int		x;
	int		y;
	char	c;
	char	o;
	char	n;

	n = 10;
	c = 32;
	x = 0;
	while (x < 9)
	{
		y = 0;
		while (y < 9)
		{
			o = array[x][y] + 48;
			write(1, &o, 1);
			if (y != 8)
				write(1, &c, 1);
			y++;
		}
		write(1, &n, 1);
		x++;
	}
}
