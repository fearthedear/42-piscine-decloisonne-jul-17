#include <unistd.h>

void	printoutput(int array[][9])
{
	int i;
	int m;
	char c;
	char o;
	char n;

	n = 10;
	c = 32;
	i = 0;
	while (i < 9)
	{	
		m = 0;
		while (m < 9)
		{
			o = array[i][m] + 48;
			write(1, &o, 1);
			write(1, &c, 1);
			m++;
		}	
		write(1, &n, 1);
		i++;
	}
}
