void	ft_error();

void	ft_data_entry(char **av)
{
	int x;
	int y;
	int array [9][9];

	x = 0;
	y = 0;
	while (y < 9 && x < 9)
	{
		if (av[y + 1][x] < 58 && av[y + 1][x] > 48)
			array[x][y] = av[y + 1][x] - 48;
		if (av[y + 1][x] == 46)
			array[x][y] = 0;
		if((av[y + 1][x] < 49 || av[y + 1][x] > 57) && (av[y + 1][x] != 46))
			return (ft_error());
		if (x == 8)
		{
			x = 0;
			y = y + 1;
		}
		else
			x++;
		}
}
