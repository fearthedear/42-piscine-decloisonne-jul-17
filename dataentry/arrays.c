int		ft_data_entry(char **av)
{
	int x;
	int y;
	int array [9][9];


	while (y < 8 && x < 8)
	{
		if (av[y][x] < 58 && av[y + 1][x] > 48)
			array[x][y] = av[y + 1][x] - 48;
		if (av[y + 1][x] == 46)
			array[x][y] = 0;
		else
		{
			ft_error();
			return(0);
		}
		if (x == 8)
		{
			x = 0;
			y = y + 1;
		}
		else
			x++;
	}
}
