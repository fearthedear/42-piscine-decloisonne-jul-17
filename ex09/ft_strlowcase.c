char	*ft_strlowcase(char *str)
{
	int		i;
	char	c;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] > 64 && str[i] < 91)
		{
			c = str[i] + 32;
			str[i] = c;
		}
		i++;
	}
	return (str);
}
