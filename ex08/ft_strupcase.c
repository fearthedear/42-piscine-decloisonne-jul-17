char	*ft_strupcase(char *str)
{
	int		i;
	char	c;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] > 96 && str[i] < 123)
		{
			c = str[i] - 32;
			str[i] = c;
		}
		i++;
	}
	return (str);
}
