int	ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb % 2 == 0)
		i = 2;
	while (i < nb / 2)
	{
		if (i * i == nb)
			return (i);
		i += 2;
	}
	return (0);
}
