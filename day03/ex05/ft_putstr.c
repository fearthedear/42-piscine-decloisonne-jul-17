int		ft_putchar(char c);

void	ft_putstr(char *str)
{
	int con;
	int n;

	con = 1;
	n = 0;
	while (con)
	{
		if (str[n] == '\0')
		{
			con = 0;
		}
		else
		{
			ft_putchar(str[n]);
		}
		n++;
	}
}
