#include <unistd.h>

int	main(void)
{
	int i;
	char a;

	i = 65;

	while (i < 91)
	{
		if (i % 2 == 1)
		{
			a = i + 32;
		}
		else
		{
			a = i;
		}
		write(1, &a, 1);
		i++;
	}
	a = '\n';
	write(1, &a, 1);
	return (0);
}	
