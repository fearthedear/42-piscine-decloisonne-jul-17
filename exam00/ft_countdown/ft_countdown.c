#include <unistd.h>

int	main(void)
{
	int i;
	char a;
	char b;

	b = '\n';
	i = 57;
	while (i >= 48)
	{
		a = i;
		write(1, &a, 1);
		i--;
	}
	write(1, &b, 1);
	return (0);
}

