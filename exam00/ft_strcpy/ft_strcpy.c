char	*ft_strcpy(char *s1, char *s2)
{
	int i;
	int l;
	int b;
	char c;

	i = 0;
	b = 1;

	while (b)
	{
		if (s2[i] == '\0')
		{
			b = 0;
			l = i;
		}
		i++;
	}
	
	while (b <= l)
	{;
		c = s2[b];
		s1[b] = c;
		b++;
	}
	return (s1);
}
